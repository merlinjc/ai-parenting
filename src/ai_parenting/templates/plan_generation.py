EVIDENCE_BASE: str = """【循证理论框架——生成时的专业锚定】

你在生成计划时，必须内化以下发展心理学和循证育儿原理，确保所有建议有理论支撑：

◉ 核心理论框架
1. Serve & Return 互动模式（Harvard Center on the Developing Child）：
   - 高质量互动的本质是"孩子发起 → 成人回应 → 孩子再回应"的循环
   - 计划中的每个互动建议都应包含"给孩子发起机会"和"成人如何接住"两个环节
   - Day 4 的"加主动性"正是从 Serve 转向让孩子主导 Serve 的关键步骤

2. 近端发展区（Vygotsky, ZPD）：
   - 计划应在"孩子能独立做到的"和"需要成人帮助才能做到的"之间找到甜蜜点
   - Day 1-2 在舒适区巩固，Day 3-5 逐步向 ZPD 边缘推进，Day 6 测试泛化能力
   - 话术中的"支架"（scaffolding）应随孩子表现动态调整——做到了就撤一点提示，卡住了就多给一点

3. 发展里程碑参考（CDC "Learn the Signs. Act Early." 2024修订版）：
   - 18-24m 关键里程碑：指物分享、回应名字、单词/双词表达萌芽、功能性游戏、简单模仿
   - 24-36m 关键里程碑：双词句→短句、两步指令理解、平行游戏→初步互动、情绪命名萌芽
   - 36-48m 关键里程碑：完整句表达、叙事串联、规则游戏参与、协商萌芽、情绪自我调节改善

4. 共同调节到自我调节的发展路径（Eisenberg, Spinrad & Eggum, 2010）：
   - 18-24m：几乎完全依赖照护者外部调节（co-regulation）
   - 24-36m：开始内化调节策略，但仍高度依赖成人提示
   - 36-48m：能在提醒下使用简单策略（深呼吸、暂停、求助），开始自我调节萌芽

◉ 生成原则
- 所有建议的核心逻辑是"创造高质量互动机会"，不是"训练特定能力"
- 每日观察点应关注"过程"（互动质量、回应方式）而非"结果"（会不会、做不做）
- 话术设计应遵循"描述-回应-等待"三拍节奏，不催促、不连续追问
- 本周节奏的设计原理：重复建立安全感（Day 1-2）→ 迁移测试灵活性（Day 3）→ 主动权转移（Day 4）→ 轻度挑战（Day 5）→ 泛化检验（Day 6）→ 反思整合（Day 7）"""

